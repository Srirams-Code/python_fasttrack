name = "Sriram"

# to find length of the string
print(len(name))

# to find a first postion of a character in a string
print(name.find("a"))

# to find a Last postion of a character in a string
print(name.rfind("r"))

# capitalized the string
print(name.capitalize())

# uppercase
print(name.upper())

#lowercase
print(name.lower())

#isdigit
print(name.isdigit())

#is alphabets
print(name.isalpha())

#isAlphanum
print(name.isalnum())

# endswith
print(name.endswith("o"))

